for i in range(0, 5):
    for j in range(0, i+1):
        print("* ",end="")
    print()
# Python Numeric Pattern Example 2
		
for i in range(0, 5):
    num = 1
    for j in range(0, i+1):
        print(num, end=" ")
        num = num + 1
    print()
#Reverse Numeric pattern
for i in range(5, 0, -1):
    for j in range(0, i):
        print(i, end="")
    print()